#인접행렬이란 2차원 배열에 각 노드가 연결된 형태를 기록하는 방식
#연결이 안된 노드끼리는 무한의 비용이라고 작성함.

#무한의 비용 
inf = 999999999

#2차원 리스트 이용해 인접 행렬 표현
graph = [
    [0,7,5],
    [7,0,inf],
    [5,inf,0] ]

print(graph)


